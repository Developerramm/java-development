package operator;

public class Relational {
    public static void main(String[] args) {
        System.out.println("Relational operator in java");
        int number1 = 20;
        int number2 = 10;
        System.out.println(number1 == number2);
        System.out.println(number1 > number2);
        System.out.println(number1 >= number2);
        System.out.println(number1 < number2);
        System.out.println(number1 <= number2);
        System.out.println(number1 != number2);
    }
}
