package operator;

public class Bitwise {
    public static void main(String[] args) {
        System.out.println("Bitwise operator in java ");
        System.out.println(5 & 5);
        System.out.println(5 | 5);
        System.out.println(5 | 8);
        System.out.println(5 & 8);
        System.out.println(~9);
        System.out.println(5 << 2);
        System.out.println(5 >> 2);
    }

}
